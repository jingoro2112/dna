void show_bitmap(unsigned char bitmap)
{
	unsigned int i;

	unsigned char counter = 1;

	i2cStartWrite( OLED_ADDRESS );

	i2cWrite( 0x80 ); // command setup
	i2cWrite( 0x00 );
	i2cWrite( 0x80 ); // command setup
	i2cWrite( 0x10 );
	i2cWrite( 0x80 ); // command setup
	i2cWrite( 0xB0 );

	for( i=0; i<512; i++ )
	{
		counter = counter + 2;
		i2cWrite( 0xC0 ); // data setup
		i2cWrite( counter );
	}

	i2cStop();
}


